package assignments;

public class Switch_case 
{

	public static void main(String[] args) 
	{
		int month=4;
		switch(month)
		{
		case 1:
		System.out.println("Jan");
		break;
		case 2:
        System.out.println("feb");
        break;
		case 3:
	     System.out.println("March");
	     break;
	        
		case 4:
	     System.out.println("April");
	       break;
	      default:
	    	  System.out.println("Month not found");
        
	}

}
}
